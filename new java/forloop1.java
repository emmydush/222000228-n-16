//this program displays  forloop 
public class forloop1 {
    public static void main(String[] args) {
        int sum = 0;// Initialize a variable 'sum' to store the sum of numbers.

        for (int i = 1; i <= 10; i++) {
            sum += i;// Add the current value of 'i' to 'sum' in each iteration.
        }
        System.out.println("sum of numbers from 1 to 10:" + sum);// Print the sum of numbers from 1 to 10.
    }

}
